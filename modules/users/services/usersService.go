package services

import (
	"strconv"

	"github.com/alejmendez/goApiRest/core/database"
	model "github.com/alejmendez/goApiRest/modules/users/models"
	"github.com/alejmendez/goApiRest/modules/users/utils"

	"github.com/dgrijalva/jwt-go"
	"github.com/jinzhu/gorm"
)

func ValidUser(id string, p string) bool {
	db := database.DB
	var user model.User
	db.First(&user, id)
	if user.Username == "" {
		return false
	}
	if !utils.CheckPasswordHash(p, user.Password) {
		return false
	}
	return true
}

func ValidToken(t *jwt.Token, id string) bool {
	n, err := strconv.Atoi(id)
	if err != nil {
		return false
	}

	claims := t.Claims.(jwt.MapClaims)
	uid := int(claims["user_id"].(float64))

	if uid != n {
		return false
	}

	return true
}

func GetUserByEmail(e string) (*model.User, error) {
	db := database.DB
	var user model.User
	if err := db.Where(&model.User{Email: e}).Find(&user).Error; err != nil {
		if gorm.IsRecordNotFoundError(err) {
			return nil, nil
		}
		return nil, err
	}
	return &user, nil
}

